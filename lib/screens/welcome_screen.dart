    FirebaseAuth.instance
        .authStateChanges()
        .listen((User user) {
      if (user == null) {
        //Navigator.pushNamed(context,WelcomeScreen.id);
      } else {
        setState(() {
          isgo=true;
          Navigator.pushReplacementNamed(context,homepage.id);

        });
      }
    });
  }

  @override
  void initState() {
    setState(() {
      signinstatus();
    });
  }

  @override
